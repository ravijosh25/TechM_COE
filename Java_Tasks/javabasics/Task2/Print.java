public class Print {
    public static void print() {
        System.out.print("Welcome to the world of coding magic!");
        System.out.print("Let's keep printing without a line break.");
    }

    public static void println() {
        System.out.println("Switching to println to start fresh on the next line.");
        System.out.println("This is the final println statement of the day.");
    }

    public void error() {
        System.err.print("Warning: Unexpected glitch detected!");
        System.err.println("Critical alert! Please resolve the issue immediately.");
    }
}
